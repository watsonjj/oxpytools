def get_pixel_id(module, asic, channel):
    """
    Convert module, asic, channel into pixel_id

    Parameters
    ----------
    module : int
    asic : int
    channel : int

    Returns
    -------
    pixel_id : int

    """

    array = np.array(chec_pixel_id).reshape(32, 4, 16)
    pixel_id = array[module][asic][channel]

    return pixel_id


def get_pixel_pos(pixel_id):
    """
    Obtain the pixel positions for a pixel_id

    Parameters
    ----------
    pixel_id : int

    Returns
    -------
    pixel_pos : pair

    """

    array = chec_pixel_pos
    pixel_pos = array[pixel_id]

    return pixel_pos


def get_all_pixel_pos():
    """
    Obtain the pixel positions for the whole camera

    Returns
    -------
    array-like pair
        numpy array of all x positions, and a numpy array of all y positions

    """

    npix = len(chec_pixel_pos)
    pos_x = np.zeros(npix,dtype=np.double)
    pos_y = np.zeros(npix,dtype=np.double)
    for pix in range(npix):
        pos_x[pix], pos_y[pix] = chec_pixel_pos[pix]

    return pos_x, pos_y